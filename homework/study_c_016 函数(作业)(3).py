roman_num=dict(I=1,V=5,X=10,L=50,C=100,D=500,M=1000)

def roman(k):
    lib=list()
    num=0
    for x in k:
        lib.append(roman_num[x])

    for x in range(len(lib)-1):
        if lib[x]>=lib[x+1]:
            num+=int(lib[x])
        else:
            num-=int(lib[x])
    num+=int(lib[-1])
    print(f'转换后的结果是:{num}')

p=input('请输入一个罗马字符:')
