# 罗马数字包含 I、V、X、L、C、D、M 七种字符，分别表示数值 1、5、10、50、100、500、1000。
# 将数字 2 写做罗马字符的形式是 II（即 I + I），数字 12 写做 XII（即 X + I + I），
# 27 写做 XXVII（即 X + X + V + I + I），
# 这就有点像咱平时上市场买菜给钱的算法（一个大白菜 27 元，那么就给两张10元，一张5元和两张1元这样的组合）。
# 罗马字符的组合也存在一些特殊规则，
# 比如 4 不写做 IIII，而是 IV（即 I 在 V 的左边，表示 5 - 1，因此得到数字 4），类似的特例如下：
#
# I 可以放在 V（5）和 X（10）的左边，表示 4 和 9
# X 可以放在 L（50）和 C（100）的左边，表示 40 和 90
# C 可以放在 D（500）和 M（1000）的左边，表示 400 和 900

